new Vue({
    el: '#header',
    data: {
        Fname: '',
        Sname: '',
        RegNo: '',
        on: true
    },
    methods: {
        addName(){
            if(this.Fname != 'Gabriel' || this.Sname != 'Frank' || this.RegNo != '123/01'){
                this.Fname = '';
                this.Sname = '';
                this.RegNo = '';
                alert('invalid student ID');
            }
            else{
                this.on = false;
            }
        }
    }
})
new Vue({
    el: "#app",
    data: {
        name: "compilation sheet",
        subject: "",
        ca1: "",
        ca2: "",
        exam: "",
        done: false,
        total: ""
    },
    methods: {
        add(){
            this.total = (+this.ca1 + +this.ca2 + +this.exam)/ 3;
            if(this.total >= 100 || this.subject == ""  || this.ca11 == ""  || this.ca12 == ""  || this.exam == "" ){
                alert("invalid");
                this.total = "";
            } 
            else{
                this.done = true;
                Math.floor(this.total); 
            }
        }
    }
})
